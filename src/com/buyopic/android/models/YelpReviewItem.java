package com.buyopic.android.models;


public class YelpReviewItem {

	private String mName;
	private String mImageUrl;
	private String mReviewCount;
	private String mReviewImageUrl;
	private String mAddress;
	public String getmName() {
		return mName;
	}
	public void setmName(String mName) {
		this.mName = mName;
	}
	public String getmImageUrl() {
		return mImageUrl;
	}
	public void setmImageUrl(String mImageUrl) {
		this.mImageUrl = mImageUrl;
	}
	public String getmReviewCount() {
		return mReviewCount;
	}
	public void setmReviewCount(String mReviewCount) {
		this.mReviewCount = mReviewCount;
	}
	public String getmReviewImageUrl() {
		return mReviewImageUrl;
	}
	public void setmReviewImageUrl(String mReviewImageUrl) {
		this.mReviewImageUrl = mReviewImageUrl;
	}
	public String getmAddress() {
		return mAddress;
	}
	public void setmAddress(String mAddress) {
		this.mAddress = mAddress;
	}
	
	
	
}
